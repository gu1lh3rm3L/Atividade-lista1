#!/bin/bash

echo -e '$PATH' "variável que indica tranjetória dos executáveis dos programas.\n"

echo -e '$HOME' "variável que guarda o valor do diretório inicial do usuário atual.\n"

echo -e '$SHELL' "variável que descreve o shell que irá interpretar todos os comandos que você digitar.\n"

echo -e '$*' "variavel que contém todos os argumentos passado para o programa.\n"

echo -e '$0' "variavel que retorna o nome do script que está sendo executado.\n"

echo -e '$#' "variavel que retorna o número de parâmetros passados.\n"

echo -e '$USER' "variavel que retorna o usuario que está atualmente conectado.\n"

echo -e '$PWD' variavel que retorna o diretório atual onde está.
