#!/bin/bash


echo -e "Substiuição de variavel:\n" Substitui a variavel pelo seu valor usando '${variavel}'."\n" Ex:Se x=10 '${x}' será substituido por valor 10. 

echo -e "Substituição de shell:\n" Substitui um comando pelo seu reseltado usando '$(comando)'."\n" Ex: '$(date)' será substituido por "$(date)"

echo -e "Substituição aritimetrica:\n" Substitui a expressão número pelo seu resultado usando '$((expressao))'."\n" Ex: '$((10 + 10))' será substituido por 20.
